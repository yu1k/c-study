#include <stdio.h>

int main(){
    int ans;
    
    ans = 20 + 10;
    printf("ans=%d\n", ans);
    ans = 20 - 10;
    printf("ans=%d\n", ans);
    ans = 20 * 10;
    printf("ans=%d\n", ans);
    ans = 20 / 10;
    printf("ans=%d\n", ans);
    ans = 20 % 10;
    printf("ans=%d\n", ans);
    return 0;
}